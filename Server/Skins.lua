Hats = Assets.GetStaticMeshes("polygon-hats")
function AddHatToCharacter(character)
	character:AddStaticMeshAttached(
		"head",
		"polygon-hats::" .. Hats[math.random(#Hats)].key,
		"head",
		Vector(5, 0, 0),
		Rotator(-90, 0, 0)
	)
end

