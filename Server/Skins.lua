function GetRandomAnimation()
	return Animation[math.random(#Animation)]
end

function SetCharacterAddBeard(character)
	if math.random() > 0.5 then
		return
	end
	for i = 1, #Beard do
		if math.random() > 0.5 then
			character:AddStaticMeshAttached("beard", Beard[math.random(#Beard)], "beard")
		end
	end
end

